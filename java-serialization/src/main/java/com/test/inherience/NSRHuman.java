package com.test.inherience;

public class NSRHuman {
	String gender;
	 String color;

	 NSRHuman() {
	 }

	 NSRHuman(String gender, String color) {
	  this.gender = gender;
	  this.color = color;
	 }

	 public String getGender() {
	  return gender;
	 }

	 public void setGender(String gender) {
	  this.gender = gender;
	 }

	 public String getColor() {
	  return color;
	 }

	 public void setColor(String color) {
	  this.color = color;
	 }
}
